/**
 * Pure CPU-bound hot paths: text chunking (embeddings ingest), wikilink parsing
 * (artifact link resolution), and GitHub-token crypto. These run per artifact /
 * per knowledge doc / per git-auth touch — cheap individually, hot in aggregate.
 */
import type { Suite } from "../harness";
import { chunkText } from "../../../server/src/lib/chunking";
import { parseWikilinks, slugify } from "../../../server/src/lib/wikilinks";
import { encrypt, decrypt } from "../../../server/src/lib/crypto";

// A representative markdown doc: prose + code fences + wikilinks, ~4KB.
const PARA =
  "The dependency graph is generated by walking the workspace roots and " +
  "resolving import specifiers against tsconfig aliases and workspace globs. " +
  "Cycles are detected with an iterative Tarjan SCC pass so large graphs stay safe.\n\n";
const MD_DOC =
  Array.from(
    { length: 8 },
    (_, i) => `## Section ${i}\n\n${PARA}See [[Spec ${i}]] and [[Design Doc|the design]].`,
  ).join("\n\n") +
  "\n\n```ts\nfunction noise() { return [[not a link]]; }\n```\n\n" +
  PARA.repeat(3);

const BIG_DOC = MD_DOC.repeat(12); // ~50KB — a large spec / captured transcript

const SECRET = "ghp_" + "a1B2c3D4e5F6g7H8".repeat(3); // realistic GitHub PAT shape
const CIPHERTEXT = encrypt(SECRET);

export const pureSuite: Suite = {
  name: "pure — chunking / wikilinks / crypto",
  cases: [
    { name: "chunkText (4KB markdown)", fn: () => chunkText(MD_DOC) },
    { name: "chunkText (50KB doc)", fn: () => chunkText(BIG_DOC) },
    { name: "parseWikilinks (4KB markdown)", fn: () => parseWikilinks(MD_DOC) },
    { name: "slugify", fn: () => slugify("My Long Spec Name.md") },
    { name: "encrypt (GitHub PAT)", fn: () => encrypt(SECRET) },
    { name: "decrypt (GitHub PAT)", fn: () => decrypt(CIPHERTEXT) },
  ],
};
